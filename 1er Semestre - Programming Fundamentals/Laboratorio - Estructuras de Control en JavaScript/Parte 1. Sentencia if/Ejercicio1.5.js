//Verifica si una cadena tiene más de 5 carácteres.

let cadenaIngresada = prompt ("Ingresa los carácteres que quieras.");
//La condición .length lee cuántos carácteres tiene la cadena que se ingresa.
if (cadenaIngresada.length > 5){
    //Si la cadena ingresada tiene más de 5 carácteres, la consola mostrará que la cadena ingresada tiene más de 5 carácteres.
    console.log(`La cadena que ingresaste tiene más de 5 carácteres.`);
}

