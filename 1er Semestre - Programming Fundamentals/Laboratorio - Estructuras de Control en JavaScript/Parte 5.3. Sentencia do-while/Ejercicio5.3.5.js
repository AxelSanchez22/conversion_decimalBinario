//Imprime los números del 10 al 1 en orden inverso.

//Se define una variable con el valor de 10.
let numero = 10;
//Se ejecuta el código primero y luego se evalúa la condición.
do{
    //Se imprime el valor de la variable.
    console.log(numero);
    //Decrementa el valor de la variable por cada ciclo que haga.
    numero --;
//Se repite el ciclo hasta que ya no cumpla la condición.
}while (numero >= 1);