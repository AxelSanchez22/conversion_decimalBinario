//Imprime los números impares entre 1 y 20.

//Se define una variable con el valor de 1.
let numeroImpar = 1;
//Mientras el numero sea menos o igual a 20,
while (numeroImpar <= 20){
    //la consola imprimirá el valor del número.
    console.log(numeroImpar);
    //El valor del número se irá incrementando por 2 cada ciclo. 
    numeroImpar += 2;
}