//Imprime los números del 10 al 1 en orden inverso.

//Se define una variable con un valor de 10.
let numero = 10;
//Mientras el número sea mayor o igual a 1,
while (numero >= 1){
    //La consola va a imprimir el valor del número.
    console.log(numero);
    //Cada ciclo el valor del número se irá decrementando por 1 hasta llegar a 1.
    numero --;
}