//Suma los primeros 10 números.

//Se define una variable con el valor de 0.
let suma = 0;
//Se define otra variable con un valor de 1.
let numero = 1;
//Mientras el número sea menor o igual a 10,
while (numero <= 10){
    //Se realizará la operación de la suma más el valor del número.
    suma += numero;
    //El valor del número se irá incrementando por 1 cada ciclo.
    numero++;
    //La consola irá imprimiendo la suma de los números hasta sumar los 10 primeros números. 
    console.log(suma);
}