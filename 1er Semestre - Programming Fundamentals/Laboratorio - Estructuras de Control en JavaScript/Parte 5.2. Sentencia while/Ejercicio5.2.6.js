//Imprime la tabla de multiplicar del 7.

//Se define una variable con un valor de 1.
let numero = 1;
//Mientras el número sea menor o igual a 12,
while (numero <= 12 ){
    //La consola va a imprimir el valor del número con las concatenaciones más la operación del valor del número por 7.
    console.log(numero + " x 7 " + " = " + (numero * 7));
    //Cada ciclo el valor del número se irá incrementando por 1 hasta llegar a 12.
    numero ++;
}