//Cuenta cuántos números entre 1 y 50 son divisibles por 5.

//Se define una variabe con el valor de 5.
let numeroDivisible = 5;
//Mientras el número sea menos o igual a 50,
while(numeroDivisible <= 50){
    //La consola imprimirá el número.
    console.log(numeroDivisible);
    //Por cada ciclo, el valor de la variable se irá incrementando por 5.
    //Esto se hace ya que cada número que se le incrementa 5, es divisible por 5.
    numeroDivisible += 5;
}