//Determina si un número es mayor que 100.

let numeroIngresado = prompt ("Ingresa cualquier número.");
//El número que se ingresa es evaluado si es mayor que 100,
if (numeroIngresado > 100){
    //Si el número ingresado es mayor que 100, la consola mostrará que el número ingresado es mayor que 100.
    console.log(`El número ${numeroIngresado} es mayor a 100.`);
}else{
    //Si el número ingresado es menor que 100, la consola mostrará que el número ingresado no es mayor que 100.
    console.log(`El número ${numeroIngresado} no es menor que 100.`);
}

/*Si el número ingresado es igual que 100, la consola mostrará que el número ingresado no es mayor que 100, 
ya que 100 es igual a 100 y no es mayor que 100. */